import OpenAI from "openai";
import dotenv from "dotenv";
dotenv.config();

const openai = new OpenAI({
	apiKey: process.env.OPENAI_API_KEY
});

export const generateYapsterReply = async (req, res) => {
	try {
		const { message } = req.body;

		if (!message || typeof message !== "string") {
			return res.status(400).json({ error: "Message is required and must be a string" });
		}

		const response = await openai.chat.completions.create({
			model: "gpt-3.5-turbo",
			messages: [
				{
					role: "system",
					content: "You are Yapster, an intelligent and helpful chatbot assistant inside a messaging app.",
				},
				{
					role: "user",
					content: message,
				},
			],
			temperature: 0.7,
			max_tokens: 100,
		});

		const reply = response?.choices?.[0]?.message?.content;

		if (!reply) {
			throw new Error("No reply generated by OpenAI");
		}

		return res.status(200).json({ message: reply.trim() });
	} catch (error) {
		console.error("Yapster AI Error:", error.message);
		return res.status(500).json({ error: "Yapster Bot failed to respond." });
	}
};
